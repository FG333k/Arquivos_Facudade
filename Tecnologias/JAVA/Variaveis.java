public class Variaveis {
    public static void main(String[] args){

        // Atribuição de variaveis      sintaxe:  tipo + espaço + nome + = + valor
        // Variavel do tipo string
        String nome = "Felipe ";     
        System.out.println(nome);

        // Também é possível mesclar variaveis string
        String sobrenome = "Almeida";
        System.out.println(nome + sobrenome);

        // Variavel do tipo numero inteiro
        int numero = 15;
        System.out.println(numero);

        // Atribuição de valor depois da declaração
        int outroNumero;
        outroNumero = 20;
        System.out.println(outroNumero);

        // Constantes
        //final int const = 100;
        //System.out.println(const);

        // Outros tipos:
        float numFloat = 5.99f;
        char letra = 'A';
        boolean boleano = true;
        System.out.println(numFloat);
        System.out.println(letra);
        System.out.println(boleano);

    }
}
