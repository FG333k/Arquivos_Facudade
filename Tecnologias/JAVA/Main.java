public class Main {   // O código sempre deve começar com uma classe e o nome dela deve ser o nome do arquivo
  public static void main(String[] args) {    // Também deve haver sempre um main

    // Exiibindo palavras no console
    System.out.println("Olá mundo");
    System.out.println("Estou aprendendo Java!");
    System.out.println("System.out.println(Olá mundo!);");

    System.out.println(".");

    // Exibindo números no console
    System.out.println(3);
    System.out.println(358);
    System.out.println(50000);

    // Calculos matemáticos
    System.out.println(2 + 3);
    System.out.println(2 * 5);
  }
}