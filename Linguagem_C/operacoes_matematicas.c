#include<stdio.h>
#include<stdlib.h>


void main(){

    //Definindo variaveis
    int a = 6, b = 3;

    //soma
    printf("\n A soma de %d e %d = %d",a,b, a + b);

    //subitaçao
    printf("\n A subitracao de %d e %d = %d",a,b, a - b);

    //mutiplicação
    printf("\n A mutiplicacao de %d e %d = %d",a,b, a * b);

    //resto da divisão
    printf("\n O resto da divisao entre  %d e %d = %d",a,b, a % b);

    //valor absoluuto
    printf("\n O valor absolido de -3 = %d", abs(-3));
}
